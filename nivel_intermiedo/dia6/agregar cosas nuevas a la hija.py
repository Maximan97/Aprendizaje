# La clase hija puede tener sus propios métodos o atributos, además de 
# los heredados.
# No está limitada por la clase padre.

# 🧪 Ejercicio 2 – Practicar:

# Agregá a la clase Perro un método ladrar() que muestre algo 
# como "¡Guau!"

