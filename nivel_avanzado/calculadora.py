def sumar(numero: int, numero1: int):
    if numero != int and numero1 != int:
        print("No son numeros wey")
    else:
        sumar = (numero + numero1)
    print(f"la suma es: {sumar}")


def restar(numero: int, numero1: int):
    restar = (numero - numero1)
    print(f"Tú resta es: {restar}")

def multiplicar(numero: int, numero1: int):
    multiplicar = (numero * numero1)
    print(f"Tú multiplo es: {multiplicar}")

def dividir(numero: int, numero1: int):
    dividir = (numero / numero1)
    print(f"Tu dividendo es: {dividir}")